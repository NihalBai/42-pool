#include <unistd.h>

int	main(int ac, char	**arv)
{
	int	i;
	int	j;

	if (ac >= 2)
	{
		i = 1;
		while (i < ac)
		{
			j = 0;
			while (arv[i][j] != '\0')
			{
				write(1, &arv[i][j], 1);
				j++;
			}
			write(1, "\n", 1);
			i++;
		}
	}
}
