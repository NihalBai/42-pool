#include <unistd.h>

int	main(int ac, char	**arv)
{
	int	i;

	(void)ac;
	i = 0;
	while (arv[0][i] != '\0' )
	{
		write(1, &arv[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
}
