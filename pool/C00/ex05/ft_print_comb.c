#include <unistd.h>

void	ft_print_comb(void)
{
	int	n;
	int	m;
	int	z;

	n = '0';
	while (n <= '7')
	{
		m = n + 1;
		while (m <= '8')
		{
			z = m + 1;
			while (z <= '9')
			{
				write(1, &n, 1);
				write(1, &m, 1);
				write(1, &z, 1);
				if (!(n == '7' && m == '8' && z == '9'))
					write(1, ", ", 2);
				z++;
			}
			m++;
		}
		n++;
	}
}
